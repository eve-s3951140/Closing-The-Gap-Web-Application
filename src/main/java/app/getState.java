package app;

public class getState {
    public String name;
    public String state;
    
    public getState() {
    }

    public getState(String name, String state) {
        this.name = name;
        this.state = state;
    }
    public String getName() {
        return name;
    }
    public String getStates() {
        return state;
    }
}
